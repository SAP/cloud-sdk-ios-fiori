struct FioriIconSetRow: View {
    let iconSet: FioriIconSet
    
    var body: some View {
        let weights: [Font.Weight] = [.ultraLight, .thin, .light, .regular, .medium, .semibold, .bold, .black, .heavy]
        VStack {
            List {
                Section(header: Text(self.iconSet.name)) {
                    ForEach(Array(self.iconSet.icons.keys), id: \.self) { iconName in
                        HStack {
                            ForEach(weights, id: \.self) { weight in
                                self.iconSet.icons[iconName]!
                                    .font(.system(size: 16, weight: weight))
                            }
                            Text(iconName)
                                .font(.fiori(forTextStyle: .footnote))
                        }
                    }
                }
            }
        }
    }
}

struct FioriIconSetCategory: View {
    let iconSet: FioriIconSet
    
    var body: some View {
        let weights: [Font.Weight] = [.ultraLight, .thin, .light, .regular, .medium, .semibold, .bold, .black, .heavy]
        List {
            ForEach(Array(self.iconSet.icons.keys).sorted(), id: \.self) { iconName in
                HStack {
                    if self.iconSet.name == "illustrations" {
                        self.iconSet.icons[iconName]!
                    } else {
                        ForEach(weights, id: \.self) { weight in
                            self.iconSet.icons[iconName]!
                                .font(.system(size: 16, weight: weight))
                        }
                    }

                    Text(iconName)
                        .font(.fiori(forTextStyle: .footnote))
                }
            }
        }
        .navigationBarTitle(self.iconSet.name.capitalized)
    }
}

